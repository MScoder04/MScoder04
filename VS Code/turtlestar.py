import turtle
turtle.Screen().bgcolor("blue")
sc=turtle.Screen()
sc.setup(400,300)
turtle.title("Welcome to Turtle")
t=turtle.Turtle()
t.forward(100)
t.left(120)
t.forward(100)
t.left(120)
t.forward(100)
t.penup()
t.right(150)
t.forward(50)
#second triangle for creating the star:
t.pendown()
t.right(90)
t.forward(100)
t.right(120)
t.forward(100)
t.right(120)
t.forward(100)
turtle.done()